  INCLUDE VICVPB.S
  AREA INTERRUPT, CODE, READONLY
  EXPORT __main
__main
	ENTRY
	LDR	R1,=IO1DIR
	LDR	R0,=0X02000000
	STR	R0,[R1]
	LDR	R1,=PINSEL1
	LDR	R0,=0X01										
	STR	R0,[R1]
	LDR	R1,=EXTMODE
	LDR	R0,=0X01
	STR	R0,[R1]
	LDR	R1,=EXTPOLAR
	LDR	R0,=0X0
	STR	R0,[R1]
	LDR	R1,=VICVectCntl0
	LDR R0,=0X2e
	STR R0,[R1]
	LDR	R1,=VICVectAddr0
	LDR	R0,=IRQ_Ent1
	STR	R0,[R1]
	LDR	R1,=VICIntEnable
	LDR	R0,=0X00004000
	STR	R0,[R1]
S	B S
IRQ_Ent1
	STMDB R13!,{R0-R1}
	LDR R1,=EXTINT
	LDR R0,=0X1
	STR R0,[R1]
	LDR R2,=0X40000000
	LDR R0,[R2]
	LDR R1,=IO1PIN
	EOR R0,R0,#0X02000000
	STR R0,[R1]
	STR R0,[R2]
	LDR R1,=VICVectAddr
	LDR R0,=0
	STR R0,[R1]
	LDMIA R13!,{r0-r1}
	SUBS PC,R14,#4
	END
		