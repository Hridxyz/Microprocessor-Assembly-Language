  INCLUDE VICVPB-1.S
	AREA CHANG, CODE, READONLY
	EXPORT __main
__main
 	ENTRY
		LDR R1,=IO0DIR
		LDR R0,=0XFF0000
		STR R0,[R1]
		LDR R2,=IO0PIN
		LDR R3,=0X0
UP		STR R3,[R2]
		ADD R3,R3,#0X10000
		CMP R3,#0XFF0000
		BGT DOWN
		B UP
DOWN	STR R3,[R2]
		SUB R3,R3,#0X10000
		CMP R3,#0X00000
		BLT UP
		B DOWN
		END
